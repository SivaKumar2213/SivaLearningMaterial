package OOPS;

public class literals {

	public static void main(String[] args) {
		//A literal exists in your source code.
		//A runtime value exists only when the program is running.
		int decimal = 10;          // Integer literal (decimal)
        int hex = 0xA;             // Hexadecimal literal
        int octal = 012;           // Octal literal
        double pi = 3.14159;       // Floating-point literal
        float rate = 5.5f;         // Float literal (with 'f')
        char letter = 'J';         // Character literal
        String text = "Java";      // String literal
        boolean flag = true;       // Boolean literal
        String nothing = null;     // Null literal
        String name="Siva";//Literal
        System.out.println(text + " literals example complete!");
        
        String name1="If the value comes from db that is runtime not in a source code that is not a literal";
	}

}
