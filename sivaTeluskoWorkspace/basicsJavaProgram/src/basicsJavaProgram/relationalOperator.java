package basicsJavaProgram;

public class relationalOperator {

	public static void main(String[] args) {
int x=5;
int y=8;
System.out.println("Lesser than operator :"+(x<y));
System.out.println("greater than operator :"+(x>y));
System.out.println("Lesser than equal operator :"+(x<=y));
System.out.println("greater than equal operator :"+(x>=y));
System.out.println("equal than operator :"+(x==y));
System.out.println("not equal than operator :"+(x!=y));
System.out.println("------------------------------------------------------------------------");
double x1=5;
float y1=5;
System.out.println("Lesser than operator :"+(x1<y1));
System.out.println("greater than operator :"+(x1>y1));
System.out.println("Lesser than equal operator :"+(x1<=y1));
System.out.println("greater than equal operator :"+(x1>=y1));
System.out.println("equal than operator :"+(x1==y1));
System.out.println("not equal than operator :"+(x1!=y1));
	}

}
