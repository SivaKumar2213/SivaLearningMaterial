package conditional;

public class ifElseif {

	public static void main(String[] args) {
int x=9;
int y=19;
int z=18;
if(x>y && x>z) {
	System.out.println("The x is greater :"+x);
}else if(y>z) {
	System.out.println("The y is greater :"+y);
}else {
	System.out.println("The z is greater :"+z);
}
	}

}
