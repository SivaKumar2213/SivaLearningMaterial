package com.learningTeleusko.basics;
//Relational operator 
//= in java is assignment operator so to compare two values is equal we need to use ==
public class basic4 {

	public static void main(String[] args) {
     double d1=8.9;
     double d2=9.8;
     int a1=10;
     int b1=10;		 
     
     System.out.println("lesser:"+(d1<d2));
     System.out.println("greater:"+(d1>d2));

     System.out.println("lesserthanequalto:"+(d1<=d2));
     System.out.println("greaterthanequalto:"+(d1>=d2));
     System.out.println("equal:"+(d1==d2));
     System.out.println("notequal:"+(d1!=d2));

     System.out.println("equal:"+(a1==b1));
     System.out.println("notequal:"+(a1!=b1));

		
		
	}

}
